#include <iostream> 
using namespace std; 

int main()
{
	int num;
	cin >> num;
	cout << num << endl;
}

/*
#include <iostream> c++에서 표준 입출력에 필요한 것들을 포함하고 있다.
c++은 헤더 파일 이름 뒤에 .h가 붙지 않는다.
using namespace std는 std라는 이름공간을 우선적으로 참조한다라는 뜻이고
처음에 using namespace std를 명시해주면 일일이 std::을 적을 필요가 없다.
cout(consol output)는 표준 출력 함수
cin(consol input)은 표준 입력 함수
endl(end line)는 한줄 띄고 화면에 출력해주는 함수
*/
